"""
Завдання 5: Календар подій
Розробити простий календар подій.
1.	Використовуючи замикання, створити функції для додавання подій, видалення подій та перегляду майбутніх подій.
2.	Зберігати події у списку за допомогою глобальної змінної.
"""

# Глобальна змінна зі списком подій
events = []


# Функція для створення календаря подій
def event_calendar():
    """
    Створює функції для керування подіями в календарі.

    Вкладені функції:
    add_event(event): Додає подію до календаря.
    remove_event(event): Видаляє подію з календаря, якщо вона існує.
    view_events(): Виводить список усіх подій у календарі.

    Повертає:
    tuple: Функції для додавання, видалення та перегляду подій.
    """
    # Функція для додавання події
    def add_event(event):
        """
        Додає нову подію до списку подій.

        Параметри:
        event (str): Назва події.

        Приклад:
        add_event("День народження")
        """
        events.append(event)
        print(f"Подію '{event}' додано до календаря")

    # Функція для видалення події
    def remove_event(event):
        """
        Видаляє подію зі списку подій, якщо вона існує.

        Параметри:
        event (str): Назва події для видалення.

        Приклад:
        remove_event("Конференція")
        """
        if event in events:
            events.remove(event)
            print(f"Подію '{event}' видалено з календаря")
        else:
            print(f"Подія '{event}' не знайдена")

    # Функція для перегляду всіх подій
    def view_events():
        """
        Виводить список усіх майбутніх подій. Якщо подій немає, виводить відповідне повідомлення.

        Приклад:
        view_events()
        """
        if events:
            print("Майбутні події:")
            for i, event in enumerate(events, start=1):
                print(f"{i}. {event}")
        else:
            print("Немає майбутніх подій")

    # Повертаємо функції у вигляді замикань
    return add_event, remove_event, view_events


# Створюємо календар подій
add_event, remove_event, view_events = event_calendar()

# Приклад використання
add_event("День народження")
add_event("Конференція")
add_event("Дедлайн")
# Перегляд подій
view_events()
remove_event("Конференція")
# Перегляд подій після видалення
view_events()
