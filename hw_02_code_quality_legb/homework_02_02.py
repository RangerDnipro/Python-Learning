"""
Завдання 2: Менеджер підписки на розсилку
Створити програму, яка імітує менеджер підписки на розсилку, демонструючи роботу з локальними, глобальними та
вкладеними областями видимості.
1.	На глобальному рівні створити змінну subscribers = [] — це список для збереження імен підписників.
2.	Створити функцію subscribe, яка приймає ім'я підписника як аргумент і додає його до списку підписників.
3.	В середині функції subscribe створити вкладену функцію confirm_subscription, яка повертає повідомлення: "Підписка
підтверджена для <ім'я>".
4.	Створити функцію unsubscribe, яка приймає ім'я та видаляє його зі списку підписників. Якщо таке ім'я не знайдено,
повертає відповідне повідомлення.
5.	Використати програму для додавання кількох підписників, підтвердження підписки та відписки.

Приклад використання:

subscribe("Олена")
subscribe("Ігор")
print(subscribers) # ['Олена', 'Ігор']
print(unsubscribe("Ігор")) # 'Ігор успішно відписаний'
print(subscribers) # ['Олена']
"""

# Створюємо глобальну змінну зі списком підписників
subscribers = []


# Функція для додавання підписника
def subscribe(name):
    """
    Додає підписника до глобального списку subscribers і підтверджує підписку.

    Параметри:
    name (str): Ім'я підписника, яке потрібно додати до списку.

    Вкладена функція:
    confirm_subscription(): Повертає повідомлення про підтвердження підписки для конкретного підписника.

    Приклад використання:
    subscribe("Олена")
    """
    # Додаємо підписника до глобального списку
    global subscribers
    subscribers.append(name)

    # Вкладена функція для підтвердження підписки
    def confirm_subscription():
        """Підтверджує підписку, повертаючи повідомлення для конкретного підписника."""
        return f"Підписка підтверджена для претендента {name}"

    # Викликаємо вкладену функцію
    print(confirm_subscription())


# Функція для відписки підписника
def unsubscribe(name):
    """
    Видаляє підписника зі списку subscribers або повертає повідомлення, якщо підписника не знайдено.

    Параметри:
    name (str): Ім'я підписника, яке потрібно видалити зі списку.

    Повертає:
    str: Повідомлення про успішне видалення або про те, що підписника не знайдено.

    Приклад використання:
    unsubscribe("Ігор")
    """    
    # Використовуємо глобальну змінну subscribers
    global subscribers

    # Перевіряємо, чи є підписник у списку
    if name in subscribers:
        subscribers.remove(name)
        return f"Підписник {name} успішно відписаний"
    else:
        return f"Підписник {name} не знайдений у списку підписників"


# Приклад використання програми
subscribe("Олена")
subscribe("Ігор")
print(subscribers)

# Відписуємо Ігоря
print(unsubscribe("Ігор"))
# Додаємо спробу відписати підписника що не існує
print(unsubscribe("Петро"))
print(subscribers)
