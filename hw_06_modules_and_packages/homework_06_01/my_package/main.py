from math_utils import factorial, gcd
from string_utils import to_uppercase, trim


def main():
    """
    Головна функція для демонстрації роботи функцій пакета
    Викликає функції для обчислення факторіала, знаходження НСД,
    перетворення тексту у верхній регістр та видалення пробілів
    """

    # Демонстрація роботи math_utils
    # Виклик функції factorial для числа 5
    print("Факторіал числа 5:", factorial(5))
    # Виклик функції gcd для двох чисел
    print("НСД для чисел 48 і 18:", gcd(48, 18))

    # Демонстрація роботи string_utils
    text = "   Hello, World!   "
    # Перетворення тексту
    print("Перетворення в верхній регістр:", to_uppercase(text))
    # Видалення зайвих пробілів
    print("Видалення пробілів:", '"' + trim(text) + '"')


if __name__ == "__main__":
    main()
