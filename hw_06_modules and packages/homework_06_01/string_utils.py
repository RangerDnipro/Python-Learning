def to_uppercase(text):
    """
    Перетворення тексту у верхній регістр
    :param text: Рядок, який потрібно перетворити
    :return: Рядок у верхньому регістрі
    """
    return text.upper()


def trim(text):
    """
    Видалення пробілів на початку та в кінці рядка
    :param text: Рядок для обробки
    :return: Рядок без пробілів на початку та в кінці
    """
    return text.strip()
